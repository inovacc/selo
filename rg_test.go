// TestRG_AuthoritativeSamples pins externally-sourced SP RG samples so the
// check-digit convention cannot silently regress. These come from independent
// published references (NOT generated by this package), which is what makes
// them meaningful: the SP/SSP-SP convention is DV = 11 - (sum mod 11) over the
// 8 base digits with ascending weights 2..9, encoding 10 as 'X' and 11 as '0'
// (as documented by NG Matemática and "Tudo em AdvPL"/siga0984). Several other
// references state the algebraically-equivalent descending form (weights 9..2
// with DV = sum mod 11): both yield the same check digit because the weight
// sets are reverses, so sum(2..9) + sum(9..2) = 11*Σdigits ≡ 0 (mod 11), hence
// (sum(9..2) mod 11) == (11 - sum(2..9) mod 11) for every input.
//
// Sources:
//   - 24.678.131-2 — "Tudo em AdvPL" (siga0984), "Algorithms – Validação de RG"
//   - 29.465.327-2 — Bóson Treinamentos, "Validação de RG SSP/SP em Python"
//
// NOTE: these are authoritative-tutorial worked examples, not confirmed
// real-person RG numbers (real RGs are not publicly verifiable, for privacy
// reasons); they independently exercise the published convention.
func TestRG_AuthoritativeSamples(t *testing.T) {
	r := NewRG()

	samples := []struct {
		value  string
		source string
	}{
		{"24.678.131-2", "siga0984 / Tudo em AdvPL"},
		{"29.465.327-2", "Bóson Treinamentos"},
	}
	for _, s := range samples {
		t.Run(s.value, func(t *testing.T) {
			if !r.Validate(s.value) {
				t.Errorf("Validate(%q) = false, want true (source: %s)", s.value, s.source)
			}
		})
	}
}

func TestRG_Format(t *testing.T) {
	r := NewRG()

	tests := []struct {
		name    string
		in      string
		want    string
		wantErr error
	}{
		{"bare digit check", "246781314", "24.678.131-4", nil},
		{"already formatted", "24.678.131-4", "24.678.131-4", nil},
		{"X check normalized", "11111111x", "11.111.111-X", nil},
		{"zero check", "111111110", "11.111.111-0", nil},
		{"too short", "1234567", "", ErrInvalidFormat},
		{"too long", "1234567890", "", ErrInvalidFormat},
		{"non-digit base", "ab.cde.fgh-1", "", ErrInvalidFormat},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got, err := r.Format(tt.in)
			if tt.wantErr != nil {
				if !errors.Is(err, tt.wantErr) {
					t.Fatalf("Format(%q) err = %v, want errors.Is %v", tt.in, err, tt.wantErr)
				}

				return
			}

			if err != nil {
				t.Fatalf("Format(%q) unexpected err: %v", tt.in, err)
			}

			if got != tt.want {
				t.Errorf("Format(%q) = %q, want %q", tt.in, got, tt.want)
			}
		})
	}
}
func TestRG_GenerateRoundTrip(t *testing.T) {
	r := NewRG()
	for range 1000 {
		v := r.Generate()
		if !r.Validate(v) {
			t.Fatalf("Generate() produced invalid RG: %q", v)
		}
		// Generated value must be in masked form XX.XXX.XXX-C.
		if len(v) != 12 || v[2] != '.' || v[6] != '.' || v[10] != '-' {
			t.Fatalf("Generate() not masked correctly: %q", v)
		}
	}
}

