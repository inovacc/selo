func FuzzRGValidate(f *testing.F) {
	r := NewRG()

	f.Add("24.678.131-4")
	f.Add("246781314")
	f.Add("11.111.111-X")
	f.Add("")
	f.Add("garbage-input")
	f.Fuzz(func(t *testing.T, s string) {
		// Must never panic; result is ignored.
		_ = r.Validate(s)
		_, _ = r.Format(s)
		_, _ = r.ValidateUF(s, UFSP)
	})
}

